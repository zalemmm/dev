function AddBusinessDays(weekDaysToAdd) {
  // fonction jours ouvrés
  var curdate = new Date();
  var realDaysToAdd = 0;
  for(i=0; i<weekDaysToAdd; i++){
    curdate.setDate(curdate.getDate()+1);
    var estdt1 = new Date(curdate);
    var n = curdate.getDay();
    if (n == '6' || n == '0') {
